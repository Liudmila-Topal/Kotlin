package operators_and_booleans

/*
    Operators - Math ('+' for String too)
    + Addition
    - Subtraction
    * Multiplication
    / Division
    % Modulus (remainder)
    ++ Increment
 */

fun main(args:Array<String>){
    println(5 + 2)
    println("Hi " + "John")

    println(5 - 2)

    println(5 * 2)

    println(10 / 2)

    println(4 % 2)
    println(7 % 2)

    var cats = 6
    cats++
    println(cats)
    cats--
    println(cats)
}